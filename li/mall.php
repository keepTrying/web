$(function(){
    $('input.change.fr').click(function(){
        var point = parseInt($(this).prev().text());
        if($.cookie('login'=='true')){
            var user_point=$.cookie('user_point');
            if(point>=user_point){
                layer.alert('积分不足！', {
                    skin: 'layui-layer-molv' //样式类名
                    ,closeBtn: 0
                    }, function(index){
                      layer.close(index);
                });
            }else{
                $.post("../hotel/user/query.php",{
                    user_phone:$.cookie('user_phone'),
                    
                },function(data,status){
                    var obj = $.parseJSON(data);
                    if(obj.status=="200"){
                       $.post('../hotel/user/alter.php',{
                            user_nick:obj.data[0].user_nick,
                            user_gender:obj.data[0].user_gender,
                            user_years:obj.data[0].user_years,
                            user_email:obj.data[0].user_email,
                            user_phone:obj.data[0].user_phone,
                            user_id_num:obj.data[0].user_id_num,
                            user_name:obj.data[0].user_name,
                            user_point:(parseInt(obj.data[0].user_point)-point)+'',
                            user_img:obj.data[0].user_img,
                            page:'0',
                            num_page:'1'
                       },function(data,status){
                           var obj1 = $.parseJSON(data);
                            if(obj1.status=="200"){
                                layer.alert('兑换成功！', {
                                  skin: 'layui-layer-molv' //样式类名
                                  ,closeBtn: 0
                                }, function(index){
                                  layer.close(index);
                                });
                            }else{
                                layer.alert('兑换失败！错误码：'+obj1.status, {
                                  skin: 'layui-layer-molv' //样式类名
                                  ,closeBtn: 0
                                }, function(index){
                                  layer.close(index);
                                });
                            }
                       });
                    }else{
                        alert("query fail! code:"+obj.status);
                    }
                });
            }
        }else{
            layer.alert('请先登录！', {
              skin: 'layui-layer-molv' //样式类名
              ,closeBtn: 0
            }, function(index){
              layer.close(index);
            });
        }
    }
    );
})
